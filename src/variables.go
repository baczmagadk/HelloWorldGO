package main

import "fmt"

func main() {
	var a = "initial"
	fmt.Println("a =", a)
	fmt.Println()

	var b, c  int= 1, 2
	fmt.Println("b =", b)
	fmt.Println("c =", c)
	fmt.Println("b + c =", b + c)
	fmt.Println()

	var d bool= true
	fmt.Println("d =", d)
	fmt.Println()

	var e int
	fmt.Println("e =", e)
	fmt.Println()

	f := "short"
	fmt.Println("f =", f)

}